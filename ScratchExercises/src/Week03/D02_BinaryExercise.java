package Week03;

public class D02_BinaryExercise {

    public static void main (String[] args) {

        // Core Java - Exercise - Operators and Numbers

        // 1. Convert the following to binary numbers to integers
        //  1       = 1
        System.out.println(Integer.toBinaryString(1));
        //  8       = 1000
        System.out.println(Integer.toBinaryString(8));
        //  33      = 100001
        System.out.println(Integer.toBinaryString(33));
        // 78       =
        System.out.println(Integer.toBinaryString(78));
        // 787      =
        System.out.println(Integer.toBinaryString(787));


    }

}
